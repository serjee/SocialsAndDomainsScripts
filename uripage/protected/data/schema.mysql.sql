DROP TABLE IF EXISTS `uri_user`;
CREATE TABLE IF NOT EXISTS `uri_user` (
    `uid` INT(11) UNSIGNED NOT NULL AUTO_INCREMENT COMMENT 'User ID',
    `email` VARCHAR(50) NOT NULL COMMENT 'E-mail',
    `password` CHAR(32) NOT NULL COMMENT 'Password',
    `salt` CHAR(32) NOT NULL COMMENT 'Sucure Code',
    `role` ENUM('ADMIN','MODERATOR','USER') NOT NULL DEFAULT 'USER' COMMENT 'Role',
    `time_create` TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT 'Create Time',
    `time_update` TIMESTAMP NOT NULL DEFAULT '0000-00-00 00:00:00' COMMENT 'Last Time',
    `balance` DECIMAL(12,2) NOT NULL DEFAULT '0.00' COMMENT 'Balance',
    `code_word` VARCHAR(20) NOT NULL DEFAULT '' COMMENT 'Code Word',
    `enabled` tinyint(1) NOT NULL DEFAULT '1' COMMENT 'Enabled account - 1 is active',
    `deleted` tinyint(1) NOT NULL DEFAULT '0' COMMENT 'Deleted account - 0 is no',
    `ip` VARCHAR(15) NOT NULL DEFAULT '0' COMMENT 'IP address',
    PRIMARY KEY (`uid`),
    UNIQUE `email` (`email`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8 COLLATE=utf8_unicode_ci;

